def pm25_to_aqi(pm25: float) -> int:
    """
    –ü—Ä–∏–º–µ—Ä–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –ø–µ—Ä–µ–≤–æ–¥–∞ PM2.5 -> AQI –ø–æ —Å—Ç–∞–Ω–¥–∞—Ä—Ç—É US EPA.
    –≠—Ç–æ —É–ø—Ä–æ—â—ë–Ω–Ω–∞—è –≤–µ—Ä—Å–∏—è, –Ω–æ –¥–ª—è –ø—Ä–æ–µ–∫—Ç–∞ –¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ.
    """
    # –¢–∞–±–ª–∏—Ü–∞ –¥–∏–∞–ø–∞–∑–æ–Ω–æ–≤ (pm_low, pm_high, aqi_low, aqi_high)
    breakpoints = [
        (0.0, 12.0, 0, 50),
        (12.1, 35.4, 51, 100),
        (35.5, 55.4, 101, 150),
        (55.5, 150.4, 151, 200),
        (150.5, 250.4, 201, 300),
        (250.5, 350.4, 301, 400),
        (350.5, 500.4, 401, 500),
    ]

    for pm_low, pm_high, aqi_low, aqi_high in breakpoints:
        if pm25 <= pm_high:
            aqi = ((aqi_high - aqi_low) / (pm_high - pm_low)) * (pm25 - pm_low) + aqi_low
            return round(aqi)

    return 500  # –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ AQI


